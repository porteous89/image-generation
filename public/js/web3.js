async function initWeb3() {
  if (window.ethereum) {
    web3 = new Web3(window.ethereum);
    await window.ethereum.enable();
  } else if (window.web3) {
    web3 = new Web3(window.web3.currentProvider);
  } else {
    window.alert("Non-Ethereum browser detected. Try MetaMask!");
  }

  const networkId = 5777; // Network ID for Ganache

  const networkData = {
    // Replace with the correct address of your ERC721 contract deployed on Ganache
    address: "0x1d3de1000F62711B88e7259E787Dd523acA121E6",
    abi: myNFTABI,
  };

  if (networkData) {
    contract = new web3.eth.Contract(networkData.abi, networkData.address);
    console.log("Contract initialized:", contract);
  } else {
    window.alert("Smart contract not deployed to the detected network.");
  }
}

initWeb3();
